    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        String stela = resd.getText();
        double tela = Double.parseDouble(stela);
        switch (op){
            case "+": tela = tela + n1;
            resd.setText(String.valueOf(tela));
            break;
            
            case "-": tela = n1 - tela;
            resd.setText(String.valueOf(tela));
            break;
            
            case "*": tela = n1 * tela;
            resd.setText(String.valueOf(tela));
            break;
            
            case "/":{
            try
                    {
                        if (tela == 0)
                                {
                                    JOptionPane.showMessageDialog(this,"Divisão por zero","Erro",JOptionPane.ERROR_MESSAGE);
                                }
                        else
                                {
                                    tela = n1 / tela;
                                resd.setText(String.valueOf(tela));
                                }
                    }
            catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(this,"Digite um número","Erro",JOptionPane.ERROR_MESSAGE);
                    }
            break;}
        }
    }//GEN-LAST:event_btnResultadoActionPerformed

