    private double insereponto(double num){
        double n=num; double ndec;
        String stela = resd.getText();
        double tela = Double.parseDouble(stela);
        if (decimal) {
            cm++;
            ndec = num / (Math.pow(10,cm));
            tela = tela + ndec;
        }
        else tela = tela*10+num;
        
        return tela;
    }
